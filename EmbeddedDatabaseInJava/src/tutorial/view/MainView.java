    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        String keyword = searchField.getText();
        if(keyword.isEmpty() || jTable1.getRowCount()==0 ){
              JOptionPane.showMessageDialog(null, "Arama kısmı ya da tablo boşken arama yapılamaz!!");
        }else if (searchBtn.getText().equals("Search")){
            defaultSearchTableModel =new DefaultTableModel();
            defaultSearchTableModel.setColumnIdentifiers(new String[]{"MOVIE ID", "MOVIE NAME", "MOVIE LINK", "MOVIE CATEGORY"});
            for (int i = 0; i < jTable1.getRowCount(); i++) {
                if(keyword.equals(jTable1.getValueAt(i, 3))){
                    Vector<Object> v = new Vector();
                    v.addElement(jTable1.getValueAt(i, 0));
                    v.addElement(jTable1.getValueAt(i, 1));
                    v.addElement(jTable1.getValueAt(i, 2));
                    v.addElement(jTable1.getValueAt(i, 3));
                    defaultSearchTableModel.addRow(v);
                }
            }
            jTable1.setModel(defaultSearchTableModel);
            searchBtn.setText("Get All Back");
        }
        else{
        jTable1.setModel(defaultTableModel);
        searchBtn.setText("Search");
        }
    }//GEN-LAST:event_searchBtnActionPerformed

