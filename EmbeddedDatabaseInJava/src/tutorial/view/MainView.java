    public final void setUpJTableModel() {
        defaultTableModel = new DefaultTableModel();
        jTable1.setModel(defaultTableModel);
        defaultTableModel.addColumn("MOVIE ID");
        defaultTableModel.addColumn("MOVIE NAME");
        defaultTableModel.addColumn("MOVIE LINK");
        defaultTableModel.addColumn("MOVIE CATEGORY");
        refreshTable();
    }

    private void refreshTable() {

        deleteBtn.setEnabled(false);
        editBtn.setEnabled(false);

        defaultTableModel.getDataVector().clear();

        ArrayList<Movie> movies = refreshRecord();

        movies.stream().forEach((currentMovie) -> {
            defaultTableModel.addRow(new Object[]{currentMovie.getMovieId(), currentMovie.getMovieName(), currentMovie.getMovieLink(), currentMovie.getMovieCategory()});
        });
        defaultTableModel.fireTableDataChanged();
    }

    private ArrayList<Movie> refreshRecord() {
        return databaseManager.getAllMovies();
    }

    @Override
    public void saveMovie(Movie movie) {
        databaseManager.addMovie(movie);
        refreshTable();
    }

    @Override
    public void editMovie(Movie movie) {
        databaseManager.updateMovie(movie);
        refreshTable();
    }
